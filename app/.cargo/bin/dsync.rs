// Create a new file .cargo/bin/dsync.rs
use std::collections::HashMap;
use std::path::PathBuf;

pub fn main() {
    let dir = env!("CARGO_MANIFEST_DIR");

    println!("Running dsync (generating model code from `backend/schema.rs`)");

    let schema_file = PathBuf::from_iter([dir, "backend/schema.rs"]);
    let models_dir = PathBuf::from_iter([dir, "backend/models"]);

    ensure_schema_is_generated(&schema_file);

    dsync::generate_files(
        schema_file,
        models_dir,
        dsync::GenerationConfig {
            connection_type: "crate::server::connection::Connection".to_string(),
            default_table_options: dsync::TableOptions::default().tsync().autogenerated_columns(vec![
                "id",
                "created_at",
                "updated_at",
            ]),
            table_options: HashMap::from([
                // plugin_storage
                ("attachment_blobs", dsync::TableOptions::default().ignore()),
                ("attachments", dsync::TableOptions::default().ignore()),
                // plugin_auth
                ("role_permissions", dsync::TableOptions::default().ignore()),
                ("user_permissions", dsync::TableOptions::default().ignore()),
                ("user_roles", dsync::TableOptions::default().ignore()),
                ("user_sessions", dsync::TableOptions::default().ignore()),
                ("users", dsync::TableOptions::default().ignore()),
                ("user_oauth2_links", dsync::TableOptions::default().ignore()),
                // plugin_tasks
                ("fang_tasks", dsync::TableOptions::default().ignore()),
                ("solr_database_info", dsync::TableOptions::default().ignore()),
            ]),
            model_path: "crate::models::".to_string(),
            schema_path: "crate::schema::".to_string(),
        },
    );
}

pub fn ensure_schema_is_generated(schema_file: &PathBuf) {
    // check that the diesel schema file is not empty
    let content = std::fs::read_to_string(schema_file).unwrap_or_default();

    if content.trim().is_empty() {
        println!("=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=");
        println!(" The diesel schema file is not generated yet.");
        println!(" If this is a new project, did you forget\n the `diesel database setup` step?");
        println!("=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=");
        panic!("`backend/schema.rs` is empty or not present");
    }
}